import React, { useState, useEffect } from "react";
import api from "../api";

// Principal Signature Management Component
const PrincipalSignaturePanel = () => {
  const [signatureStatus, setSignatureStatus] = useState(null);
  const [loading, setLoading] = useState(true);
  const [uploading, setUploading] = useState(false);
  const [error, setError] = useState("");

  useEffect(() => {
    fetchSignatureStatus();
  }, []);

  const fetchSignatureStatus = async () => {
    try {
      setLoading(true);
      const response = await api.get("/admin/clubs/principal_signature/");
      setSignatureStatus(response.data);
    } catch (err) {
      setError("Failed to load signature status");
      console.error("Error loading principal signature:", err);
    } finally {
      setLoading(false);
    }
  };

  const handleFileUpload = async (event) => {
    const file = event.target.files[0];
    if (!file) return;

    // Validate file type
    if (!file.type.startsWith('image/')) {
      setError('Please select a valid image file');
      return;
    }

    // Validate file size (max 5MB)
    if (file.size > 5 * 1024 * 1024) {
      setError('File size must be less than 5MB');
      return;
    }

    setUploading(true);
    setError("");

    try {
      const formData = new FormData();
      formData.append('signature', file);

      const response = await api.post("/admin/clubs/principal_signature/", formData, {
        headers: {
          'Content-Type': 'multipart/form-data',
        },
      });

      setSignatureStatus(response.data);
      setError("");
    } catch (err) {
      setError(err.response?.data?.error || 'Failed to upload signature');
      console.error("Error uploading signature:", err);
    } finally {
      setUploading(false);
    }
  };

  if (loading) {
    return <div className="p-4 text-center">Loading signature status...</div>;
  }

  return (
    <div className="space-y-4">
      {error && (
        <div className="p-3 bg-red-100 border border-red-400 text-red-700 rounded">
          {error}
        </div>
      )}

      <div className="flex items-center justify-between p-4 bg-gray-50 rounded-lg">
        <div>
          <h3 className="text-lg font-semibold text-gray-800">
            Principal: {signatureStatus?.principal_name || 'Not Found'}
          </h3>
          <p className="text-sm text-gray-600">
            This signature will be used on certificates generated by clubs
          </p>
        </div>

        <div className="flex items-center gap-4">
          {signatureStatus?.has_signature ? (
            <div className="text-green-600 flex items-center gap-2">
              <span className="text-sm">âœ“ Signature Available</span>
              {signatureStatus.signature_url && (
                <img
                  src={signatureStatus.signature_url}
                  alt="Principal Signature"
                  className="h-12 w-auto border border-gray-300 rounded"
                />
              )}
            </div>
          ) : (
            <span className="text-red-600 text-sm">âš  No signature uploaded</span>
          )}

          <label className="px-4 py-2 bg-blue-600 text-white rounded-lg hover:bg-blue-700 cursor-pointer transition-colors">
            <input
              type="file"
              accept="image/*"
              onChange={handleFileUpload}
              disabled={uploading}
              className="hidden"
            />
            {uploading ? 'Uploading...' : (signatureStatus?.has_signature ? 'Update Signature' : 'Upload Signature')}
          </label>
        </div>
      </div>
    </div>
  );
};

